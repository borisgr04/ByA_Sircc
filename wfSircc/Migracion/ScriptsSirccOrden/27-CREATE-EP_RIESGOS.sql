COMMENT ON COLUMN EP_RIESGOS.TIPO IS 'Tipo de Riesgo';

COMMENT ON COLUMN EP_RIESGOS.DESCRIPCION IS 'Descripción del Riesgo';

COMMENT ON COLUMN EP_RIESGOS.CONSECUENCIAS IS 'Consecuencias del Riesgo';

COMMENT ON COLUMN EP_RIESGOS.PROBABILIDAD_R IS 'Probabilidad del Riesgo';

