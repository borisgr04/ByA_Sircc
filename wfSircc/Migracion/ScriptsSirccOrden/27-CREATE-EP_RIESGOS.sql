CREATE TABLE EP_RIESGOS
(
  ID                  NUMBER,
  N                   NUMBER,
  ID_EP               NUMBER,
  CLASE               VARCHAR2(20 BYTE),
  FUENTE              VARCHAR2(10 BYTE),
  ETAPA               VARCHAR2(20 BYTE),
  TIPO                VARCHAR2(50 BYTE),
  DESCRIPCION         VARCHAR2(100 BYTE),
  CONSECUENCIAS       VARCHAR2(100 BYTE),
  PROBABILIDAD_R      NUMBER(1),
  IMPACTO_R           NUMBER(1),
  VALORACION_R        NUMBER(2),
  CATEGORIA_R         VARCHAR2(50 BYTE),
  AQUIENSEASIGNA      VARCHAR2(50 BYTE),
  TRATAMIENTO         VARCHAR2(100 BYTE),
  PROBABILIDAD_T      NUMBER(1),
  IMPACTO_T           NUMBER(1),
  VALORACION_T        NUMBER(2),
  CATEGORIA_T         VARCHAR2(50 BYTE),
  AFECTAEJECUCIONCTO  VARCHAR2(2 BYTE),
  RESPONSABLE         VARCHAR2(100 BYTE),
  FECHAINICIO         DATE,
  FECHACOMPLETA       DATE,
